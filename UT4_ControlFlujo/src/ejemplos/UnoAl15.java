package ejemplos;

public class UnoAl15 {

	public static void main(String[] args) {
		int i;
		
		i=15;
		while (i>0) {
			System.out.println(i);
			i--;
		}
		
		for(int j=15;j>0;j--)
			System.out.println(j); 

		for(int k=1;k<=15;k++)
			System.out.println(15-k+1);
		
	}

}
